import math
import random

print("2 to the power of 2 is", 2**2)

print("power of 2 to the 3rd power is", pow(2, 3))

print("absolute power of -2 is", abs(-2))

print("round of 2.8 is", round(2.8))
print("round of 1.2 is", round(1.1))

print("square root of 12 is", math.sqrt(12))
print("floor of 2.5 is", math.floor(2.5))

# generate a random number between 0 and 1
print("randome number between 0 and 1 is", random.random())
# generate a random number between 1 and 100
